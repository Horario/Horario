package hft.wiinf.de.horario.model;

/**
 * Types of repetition for an event
 */
public enum Repetition {

    /**
     * No repetition.
     */
    NONE,
    /**
     * Daily repetition.
     */
    DAILY,
    /**
     * Weekly repetition.
     */
    WEEKLY,
    /**
     * Monthly repetition.
     */
    MONTHLY,
    /**
     * Yearly repetition.
     */
    YEARLY;
}
